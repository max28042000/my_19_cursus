#include "ft_printf.h"

int	ft_s(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		ft_putstr("(null)");
		return (6);
	}
	ft_putstr(str);
	while (str[i])
		i++;
	return (i);
}
